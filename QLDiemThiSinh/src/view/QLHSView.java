    private void themHoacCapNhatThiSinh(HocSinh hs) {
        DefaultTableModel modelTable = (DefaultTableModel) Table.getModel();
        if(this.model.kiemTraTonTaiTS(hs)== false && "Them".equals(luaChon)){ 
            this.model.insert(hs);
            DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            modelTable.addRow(new Object[]{hs.getMaHocSinh() + "" , hs.getTenHocSinh(),hs.getQueQuan().getTenTinh(),df.format(hs.getNgaySinh()),(hs.isGioiTinh()?"Nam":"Nữ") , hs.getDiemMieng()+"",hs.getDiem15Phut()+"",hs.getDiem1Tiet()+"",hs.getDiemCuoiKy()+""});
        }else if("Update".equals(luaChon) && this.model.kiemTraTonTaiTS(hs)== true){
            
            int i_row = Table.getSelectedRow();
            this.model.update(hs,i_row);
            int soDong = modelTable.getRowCount();
            DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            for(int i = 0 ; i <soDong ;i++){
                String id = modelTable.getValueAt(i, 0)+"";
                if(id.equals(hs.getMaHocSinh()+"")){
                    modelTable.setValueAt(hs.getMaHocSinh()+"", i, 0);
                    modelTable.setValueAt(hs.getTenHocSinh()+"", i, 1);
                    modelTable.setValueAt(hs.getQueQuan().getTenTinh()+"", i, 2);
                    modelTable.setValueAt(df.format(hs.getNgaySinh())+"", i, 3);
                    modelTable.setValueAt((hs.isGioiTinh()?"Nam":"Nữ")+"", i, 4);
                    modelTable.setValueAt(hs.getDiemMieng()+"", i, 5);
                    modelTable.setValueAt(hs.getDiem15Phut()+"", i, 6);
                    modelTable.setValueAt(hs.getDiem1Tiet()+"", i, 7);
                    modelTable.setValueAt(hs.getDiemCuoiKy()+"", i, 8);                
                }
            }
            luaChon = "";
        }
        
   }
    

    

