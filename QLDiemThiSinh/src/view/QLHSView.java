    private void timKiem() {
        this.huyTim();
        int queQuan = this.ComboBox_QueQuan_Tim.getSelectedIndex()-1;
        String maTS = this.TextField_MaSinhVIen_Tim.getText();
        DefaultTableModel modelTable = (DefaultTableModel) Table.getModel();
        int soDong = modelTable.getRowCount();
        
        Set<Integer> idCanXoa = new TreeSet<>();
        if(queQuan >= 0){
            Tinh tinhChon = Tinh.getTinhById(queQuan);
            for(int i = 0; i < soDong;i++){
                String tenTinh = modelTable.getValueAt(i, 2)+"";
                String id = modelTable.getValueAt(i, 0)+"";
                if(!tenTinh.equals(tinhChon.getTenTinh())){
                    idCanXoa.add(Integer.valueOf(id));
//                    System.out.println(id);
                }
            }
        }
        if(maTS.length() > 0){
            for(int i = 0; i < soDong; i++){
                String id = modelTable.getValueAt(i,0)+"";
                if(!id.equals(maTS)){
                    idCanXoa.add(Integer.valueOf(id));
                }
            }
        }
        if(idCanXoa.size() != soDong){
        for(Integer idXoa : idCanXoa)  {
            soDong = modelTable.getRowCount();
            for(int i = 0; i< soDong; i++){
                String idTable = modelTable.getValueAt(i, 0)+"";
               
//                System.out.println(idTable);
//                System.out.println(idXoa);
                if(idTable.equals(idXoa.toString())){
                    try {
                        modelTable.removeRow(i);
//                        System.out.println(idTable.equals(idXoa.toString()));
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                    break;
                }
            }
        }
        }else{
            JOptionPane.showMessageDialog(this, "Không tồn tại học sinh phù hợp với tìm kiếm");
        }
        
    }

